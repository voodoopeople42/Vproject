-- –†–µ—à–µ–Ω–∏–µ:
-- –ü—Ä–∏–º–µ–Ω—è–µ–º –ü—Ä–∞–≤–∏–ª–æ –î–µ –ú–æ—Ä–≥–∞–Ω–∞ –¥–ª—è –¥–ª—è —É–ø—Ä–æ—â–µ–Ω–∏—è –≤—ã—Ä–∞–∂–µ–Ω–∏—è:
-- NOT (BIRTHDAY >= '1993-01-01' AND ID > 9) = ¬¨(ùëé ‚ãÄ ùëè)= ¬¨ùëé ‚ãÅ ¬¨ùëè
/*–¢–µ–∫—Å—Ç –∑–∞–ø—Ä–æ—Å–∞*/
-- –ù–∞–ø–∏—à–∏—Ç–µ –∑–∞–ø—Ä–æ—Å –¥–ª—è –≤—ã–≤–æ–¥–∞ –≤—Å–µ—Ö –ø–æ–ª–µ–π —Ç–∞–±–ª–∏—Ü—ã STUDENTS, –≤ –∫–æ—Ç–æ—Ä–æ–º —Ä–µ–∑—É–ª—å—Ç–∏—Ä—É—é—â–∏–µ –∑–∞–ø–∏—Å–∏ —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä—è—é—Ç —Å–ª–µ–¥—É—é—â–∏–º —É—Å–ª–æ–≤–∏—è–º:
-- 1) –°—Ç–∏–ø–µ–Ω–¥–∏—è —Å—Ç—É–¥–µ–Ω—Ç–∞ –Ω–µ –ø—Ä–µ–≤—ã—à–∞–µ—Ç 500
-- "–ò–õ–ò" 
-- 2) –î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è –°—Ç—É–¥–µ–Ω—Ç–∞ –º–µ–Ω—å—à–µ '1993-01-01'
