-- –ü–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ –ø—Ä–∏–º–µ–Ω—è–µ–º –ü—Ä–∞–≤–∏–ª–∞ –î–µ –ú–æ—Ä–≥–∞–Ω–∞ –¥–ª—è —É–ø—Ä–æ—â–µ–Ω–∏—è –≤—ã—Ä–∞–∂–µ–Ω–∏—è: 
