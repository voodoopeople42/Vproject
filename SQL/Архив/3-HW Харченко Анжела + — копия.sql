--  !!! –í –≤—ã—Ö–æ–¥–Ω–æ–π –≤—ã–±–æ—Ä–∫–µ –¥–æ–ª–∂–Ω—ã –ø—Ä–∏—Å—É—Ç—Å—Ç–≤–æ–≤–∞—Ç—å —Ç–æ–ª—å–∫–æ –∑–∞–ø—Ä–∞—à–∏–≤–∞–µ–º—ã–µ –≤ —É—Å–ª–æ–≤–∏–∏ –ø–æ–ª—è.

-- 1. –ù–∞–ø–∏—à–∏—Ç–µ –æ–¥–∏–Ω –∑–∞–ø—Ä–æ—Å —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –ø—Å–µ–≤–¥–æ–Ω–∏–º–æ–≤ –¥–ª—è —Ç–∞–±–ª–∏—Ü –∏ –ø—Å–µ–≤–¥–æ–Ω–∏–º–æ–≤ –ø–æ–ª–µ–π, 
--    –≤—ã–±–∏—Ä–∞—é—â–∏–π –≤—Å–µ –≤–æ–∑–º–æ–∂–Ω—ã–µ –∫–æ–º–±–∏–Ω–∞—Ü–∏–∏ –≥–æ—Ä–æ–¥–æ–≤ (CITY) –∏–∑ —Ç–∞–±–ª–∏—Ü 
--    STUDENTS, LECTURERS –∏ UNIVERSITIES
--    —Å—Ç—Ä–æ–∫–∏ –Ω–µ –¥–æ–ª–∂–Ω—ã –ø–æ–≤—Ç–æ—Ä—è—Ç—å—Å—è, —É–±–µ–¥–∏—Ç–µ—Å—å –≤ –≤—ã–≤–æ–¥–µ —Ç–æ–ª—å–∫–æ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Ç—Ä–æ–µ–∫ –≥–æ—Ä–æ–¥–æ–≤
--    –í–Ω–∏–º–∞–Ω–∏–µ: —É–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –∫–∞–∂–¥–∞—è –∫–æ–ª–æ–Ω–∫–∞ –≤—ã–±–æ—Ä–∫–∏ –∏–º–µ–µ—Ç —Å–≤–æ–µ —É–Ω–∏–∫–∞–ª—å–Ω–æ–µ –∏–º—è
select distinct 
	S.CITY as "–ì–æ—Ä–æ–¥_–°—Ç—É–¥–µ–Ω—Ç—ã"
	,L.CITY as "–ì–æ—Ä–æ–¥_–ü—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª–∏"
	,U.CITY as "–ì–æ—Ä–æ–¥_–£–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç—ã"
from STUDENTS as S, LECTURERS as L, UNIVERSITIES as U
order by S.CITY, L.CITY, U.CITY;

-- 2. –ù–∞–ø–∏—à–∏—Ç–µ –∑–∞–ø—Ä–æ—Å –¥–ª—è –≤—ã–≤–æ–¥–∞ –ø–æ–ª–µ–π –≤ —Å–ª–µ–¥—É—â–µ–º –ø–æ—Ä—è–¥–∫–µ: —Å–µ–º–µ—Å—Ç—Ä, –≤ –∫–æ—Ç–æ—Ä–æ–º –æ–Ω
--    —á–∏—Ç–∞–µ—Ç—Å—è, –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–∞ (–Ω–æ–º–µ—Ä–∞ ID) –ø—Ä–µ–¥–º–µ—Ç–∞ –æ–±—É—á–µ–Ω–∏—è, –µ–≥–æ –Ω–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏—è –∏ 
--    –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –æ—Ç–≤–æ–¥–∏–º—ã—Ö –Ω–∞ —ç—Ç–æ—Ç –ø—Ä–µ–¥–º–µ—Ç —á–∞—Å–æ–≤ –¥–ª—è –≤—Å–µ—Ö —Å—Ç—Ä–æ–∫ —Ç–∞–±–ª–∏—Ü—ã SUBJECTS
select 
	SEMESTER	--—Å–µ–º–µ—Å—Ç—Ä
	,ID		--–∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä (–Ω–æ–º–µ—Ä ID) –ø—Ä–µ–¥–º–µ—Ç–∞ –æ–±—É—á–µ–Ω–∏—è
	,NAME --–Ω–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏–µ –ø—Ä–µ–¥–º–µ—Ç–∞ –æ–±—É—á–µ–Ω–∏—è
	,HOURS --–∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–∞—Å–æ–≤, –æ—Ç–≤–æ–¥–∏–º—ã—Ö –Ω–∞ –ø—Ä–µ–¥–º–µ—Ç–∞ –æ–±—É—á–µ–Ω–∏—è
from SUBJECTS
order by SEMESTER, ID, NAME;

-- 3. –í—ã–≤–µ–¥–∏—Ç–µ –≤—Å–µ —Å—Ç—Ä–æ–∫–∏ —Ç–∞–±–ª–∏—Ü—ã EXAM_MARKS, –≤ –∫–æ—Ç–æ—Ä—ã—Ö –ø—Ä–µ–¥–º–µ—Ç –æ–±—É—á–µ–Ω–∏—è SUBJ_ID —Ä–∞–≤–µ–Ω 4
select 
	SUBJ_ID
	/* * */ -- –∞ –º–æ–∂–Ω–æ –±—ã–ª–æ –±—ã –≤–æ—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è —Å—Ç—Ä–æ—á–Ω—ã–º –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–º
from EXAM_MARKS
where SUBJ_ID = 4;

-- 4. –ù–µ–æ–±—Ö–æ–¥–∏–º–æ –≤—ã–±–∏—Ä–∞—Ç—å –≤—Å–µ –¥–∞–Ω–Ω—ã–µ, –≤ —Å–ª–µ–¥—É—é—â–µ–º –ø–æ—Ä—è–¥–∫–µ 
--    –°—Ç–∏–ø–µ–Ω–¥–∏—è, –ö—É—Ä—Å, –§–∞–º–∏–ª–∏—è, –ò–º—è  –∏–∑ —Ç–∞–±–ª–∏—Ü—ã STUDENTS, –ø—Ä–∏—á–µ–º –∏–Ω—Ç–µ—Ä–µ—Å—É—é—Ç
--    —Å—Ç—É–¥–µ–Ω—Ç—ã, —Ä–æ–¥–∏–≤—à–∏–µ—Å—è –ø–æ—Å–ª–µ '1993-07-21'
select 
	STIPEND as "–°—Ç–∏–ø–µ–Ω–¥–∏—è"
	,COURSE as "–ö—É—Ä—Å"
	,SURNAME as "–§–∞–º–∏–ª–∏—è"
	,NAME as "–ò–º—è"
	/*,BIRTHDAY as "–î.—Ä. > '1993-07-21'"*/
from STUDENTS
where BIRTHDAY > '1993-07-21'
order by BIRTHDAY, SURNAME, NAME;

-- 5. –í—ã–≤–µ—Å—Ç–∏ –Ω–∞ —ç–∫—Ä–∞–Ω –≤—Å–µ –ø—Ä–µ–¥–º–µ—Ç—ã: –∏—Ö –Ω–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏—è –∏ –∫–æ–ª-–≤–æ —á–∞—Å–æ–≤ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –∏–∑ –Ω–∏—Ö
--    –≤ 1-–º —Å–µ–º–µ—Å—Ç—Ä–µ –∏ –ø—Ä–∏ —ç—Ç–æ–º –∫–æ–ª-–≤–æ —á–∞—Å–æ–≤ –Ω–µ –¥–æ–ª–∂–Ω–æ –ø—Ä–µ–≤—ã—à–∞—Ç—å 41
select 
	NAME as "–ü—Ä–µ–¥–º–µ—Ç"
	,HOURS "–ö–æ–ª-–≤–æ —á–∞—Å–æ–≤"
	/*,SEMESTER	*/
from SUBJECTS
where SEMESTER = 1 AND HOURS <= 41;

-- 6. –ù–∞–ø–∏—à–∏—Ç–µ –∑–∞–ø—Ä–æ—Å, –ø–æ–∑–≤–æ–ª—è—é—â–∏–π –≤—ã–≤–µ—Å—Ç–∏ –∏–∑ —Ç–∞–±–ª–∏—Ü—ã EXAM_MARKS —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ 
--    –∑–Ω–∞—á–µ–Ω–∏—è —ç–∫–∑–∞–º–µ–Ω–∞—Ü–∏–æ–Ω–Ω—ã—Ö –æ—Ü–µ–Ω–æ–∫, –∫–æ—Ç–æ—Ä—ã–µ –±—ã–ª–∏ –ø–æ–ª—É—á–µ–Ω—ã '2012-06-12'
select distinct 
	MARK as "–≠–∫–∑–∞–º–µ–Ω–∞—Ü–∏–æ–Ω–Ω—ã–µ –æ—Ü–µ–Ω–∫–∏"
	/*,EXAM_DATE*/
from EXAM_MARKS
where EXAM_DATE = '2012-06-12';

-- 7. –í—ã–≤–µ–¥–∏—Ç–µ —Å–ø–∏—Å–æ–∫ —Ñ–∞–º–∏–ª–∏–π —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, –æ–±—É—á–∞—é—â–∏—Ö—Å—è –Ω–∞ —Ç—Ä–µ—Ç—å–µ–º –∏ –ø–æ—Å–ª–µ–¥—É—é—â–∏—Ö 
--    –∫—É—Ä—Å–∞—Ö –∏ –ø—Ä–∏ —ç—Ç–æ–º –ø—Ä–æ–∂–∏–≤–∞—é—â–∏—Ö –Ω–µ –≤ –ö–∏–µ–≤–µ, –Ω–µ –•–∞—Ä—å–∫–æ–≤–µ –∏ –Ω–µ –õ—å–≤–æ–≤–µ.
select
	SURNAME
	/*,COURSE
	,CITY*/
from STUDENTS
where COURSE >= 3 AND NOT CITY IN ('–ö–∏–µ–≤', '–•–∞—Ä—å–∫–æ–≤', '–õ—å–≤–æ–≤')
order by COURSE, SURNAME;

-- 8. –ü–æ–∫–∞–∂–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ –æ —Ñ–∞–º–∏–ª–∏–∏, –∏–º–µ–Ω–∏ –∏ –Ω–æ–º–µ—Ä–µ –∫—É—Ä—Å–∞ –¥–ª—è —Å—Ç—É–¥–µ–Ω—Ç–æ–≤, 
--    –ø–æ–ª—É—á–∞—é—â–∏—Ö —Å—Ç–∏–ø–µ–Ω–¥–∏—é –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ –æ—Ç 450 –¥–æ 650, –Ω–µ –≤–∫–ª—é—á–∞—è 
--    —ç—Ç–∏ –≥—Ä–∞–Ω–∏—á–Ω—ã–µ —Å—É–º–º—ã. –ü—Ä–∏–≤–µ–¥–∏—Ç–µ –Ω–µ—Å–∫–æ–ª—å–∫–æ –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤ —Ä–µ—à–µ–Ω–∏—è —ç—Ç–æ–π –∑–∞–¥–∞—á–∏.

--–í–∞—Ä–∏–∞–µ—Ç ‚Ññ1 (–ü–æ–ª–Ω–æ—Ç–∞ = 100%)
select
	SURNAME as "–§–∞–º–∏–ª–∏—è"
	,NAME as "–ò–º—è"
	,COURSE as "–ö—É—Ä—Å"
	/*,STIPEND as "–°—Ç–∏–ø–µ–Ω–¥–∏—è"*/
from STUDENTS
where STIPEND > 450 AND STIPEND < 650
order by COURSE, STIPEND desc, SURNAME, NAME;

--–í–∞—Ä–∏–∞–µ—Ç ‚Ññ2 (–ü–æ–ª–Ω–æ—Ç–∞ = 100%)
select
	SURNAME as "–§–∞–º–∏–ª–∏—è"
	,NAME as "–ò–º—è"
	,COURSE as "–ö—É—Ä—Å"
	/*,STIPEND as "–°—Ç–∏–ø–µ–Ω–¥–∏—è"*/
from STUDENTS
where STIPEND BETWEEN 450.01 AND 649.99
order by COURSE, STIPEND desc, SURNAME, NAME;

--–í–∞—Ä–∏–∞–µ—Ç ‚Ññ3 (–ü–æ–ª–Ω–æ—Ç–∞ = 100%) -- –Ω—É —Ç—É—Ç –≤—ã —è–≤–Ω–æ –ø–µ—Ä–µ–º—É–¥—Ä–∏–ª–∏)))
select
	SURNAME as "–§–∞–º–∏–ª–∏—è"
	,NAME as "–ò–º—è"
	,COURSE as "–ö—É—Ä—Å"
	/*,STIPEND as "–°—Ç–∏–ø–µ–Ω–¥–∏—è"*/
from STUDENTS
where STIPEND NOT IN (select distinct STIPEND
						from STUDENTS
						where STIPEND <= 450 OR STIPEND >= 650)
order by COURSE, STIPEND desc, SURNAME, NAME;

-- 9. –ù–∞–ø–∏—à–∏—Ç–µ –∑–∞–ø—Ä–æ—Å, –≤—ã–ø–æ–ª–Ω—è—é—â–∏–π –≤—ã–±–æ—Ä–∫—É –∏–∑ —Ç–∞–±–ª–∏—Ü—ã LECTURERS –≤—Å–µ—Ö —Ñ–∞–º–∏–ª–∏–π
--    –ø—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª–µ–π, –ø—Ä–æ–∂–∏–≤–∞—é—â–∏—Ö –≤–æ –õ—å–≤–æ–≤–µ, –ª–∏–±–æ –∂–µ –ø—Ä–µ–ø–æ–¥–∞—é—â–∏—Ö –≤ —É–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç–µ
--    —Å –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–æ–º 14
select
	SURNAME as "–§–∞–º–∏–ª–∏—è –ü—Ä–µ–ø–æ–¥–∞–≤–∞—Ç–µ–ª—è"
	/*,CITY as "–ì–æ—Ä–æ–¥ –ø—Ä–æ–∂–∏–≤–∞–Ω–∏—è"
	,UNIV_ID as "–ò–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä –£–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç–∞"*/
from LECTURERS
where CITY = '–õ—å–≤–æ–≤' OR UNIV_ID = 14
order by SURNAME;

-- 10. –í—ã—è—Å–Ω–∏—Ç–µ –≤ –∫–∞–∫–∏—Ö –≥–æ—Ä–æ–¥–∞—Ö (–Ω–∞–∑–≤–∞–Ω–∏—è) —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω—ã —É–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç—ã,  
--     —Ä–µ–π—Ç–∏–Ω–≥ –∫–æ—Ç–æ—Ä—ã—Ö —Å–æ—Å—Ç–∞–≤–ª—è–µ—Ç 528 +/- 47 –±–∞–ª–ª–æ–≤.
select
	CITY as "–†–∞—Å–ø–æ–ª–æ–∂–µ–Ω–∏–µ –£–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç–∞"
	/*,NAME as "–£–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç"
	,RATING as "–†–µ–π—Ç–∏–Ω–≥ –£–Ω–∏–≤–µ—Ä—Å–∏—Ç–µ—Ç–∞"*/
from UNIVERSITIES
where RATING BETWEEN (528 - 47) AND (528 + 47)
order by CITY, RATING desc;

-- 11. –û—Ç–æ–±—Ä–∞—Ç—å —Å–ø–∏—Å–æ–∫ —Ñ–∞–º–∏–ª–∏–π —Å—Ç—É–¥–µ–Ω—Ç–æ–≤ —Ä–æ–¥–∏–≤—à–∏—Ö—Å—è –≤ –ö–∏–µ–≤–µ, –∏—Ö –∏–º–µ–Ω –∏ –¥–∞—Ç —Ä–æ–∂–¥–µ–Ω–∏–π 
--     –¥–ª—è –≤—Å–µ—Ö –Ω–µ—á–µ—Ç–Ω—ã—Ö –∫—É—Ä—Å–æ–≤.
select
	SURNAME as "–§–∞–º–∏–ª–∏—è"
	,NAME as "–ò–º—è"
	,BIRTHDAY as "–î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è"
	/*,CITY as "–ì–æ—Ä–æ–¥"
	,COURSE as "–ö—É—Ä—Å"*/
from STUDENTS
where 
	CITY = '–ö–∏–µ–≤'
	AND 
	COURSE%2 = 1 /*–¥–ª—è –≤—Å–µ—Ö –Ω–µ—á–µ—Ç–Ω—ã—Ö –∫—É—Ä—Å–æ–≤*/
order by COURSE, SURNAME, NAME, BIRTHDAY;

-- 12. –£–ø—Ä–æ—Å—Ç–∏—Ç–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ (–∏–∑–±–∞–≤—Ç–µ—Å—å –æ—Ç NOT) –∏ –¥–∞–π—Ç–µ –ª–æ–≥–∏—á–µ—Å–∫—É—é —Ñ–æ—Ä–º—É–ª–∏—Ä–æ–≤–∫—É –∑–∞–ø—Ä–æ—Å–∞?
