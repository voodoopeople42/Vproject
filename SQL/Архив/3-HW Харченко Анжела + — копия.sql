-- 3) –ò–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä –°—Ç—É–¥–µ–Ω—Ç–∞ –º–µ–Ω—å—à–µ –∏–ª–∏ —Ä–∞–≤–µ–Ω 9.

SELECT * 
 FROM STUDENTS 
 WHERE STIPEND < 500 OR BIRTHDAY < '1993-01-01' OR ID <= 9;

 -- 13. –£–ø—Ä–æ—Å—Ç–∏—Ç–µ –≤—ã—Ä–∞–∂–µ–Ω–∏–µ —Ñ–∏–ª—å—Ç—Ä–∞—Ü–∏–∏ (–∏–∑–±–∞–≤—Ç–µ—Å—å –æ—Ç NOT) –∏ –¥–∞–π—Ç–µ –ª–æ–≥–∏—á–µ—Å–∫—É—é —Ñ–æ—Ä–º—É–ª–∏—Ä–æ–≤–∫—É –∑–∞–ø—Ä–æ—Å–∞?
-- SELECT * FROM STUDENTS WHERE NOT ((BIRTHDAY = '1993-06-07' OR STIPEND > 500) AND ID >= 9)
-- –ü–æ–¥—Å–∫–∞–∑–∫–∞: –ø–æ—Å–ª–µ —É–ø—Ä–æ—â–µ–Ω–∏—è, –∑–∞–ø—Ä–æ—Å –¥–æ–ª–∂–µ–Ω –≤–æ–∑–≤—Ä–∞—â–∞—Ç—å —Ç—É –∂–µ –≤—ã–±–æ—Ä–∫—É, —á—Ç–æ –∏ –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω—ã–π

SELECT * 
FROM STUDENTS 
WHERE NOT ((BIRTHDAY = '1993-06-07' OR STIPEND > 500) AND ID >= 9);

